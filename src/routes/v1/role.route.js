const express = require('express');
const validate = require('../../middlewares/validate');
const auth = require('../../middlewares/auth');
const roleValidation = require('../../validations/role.validation');
const roleController = require('../../controllers/role.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('admin'), validate(roleValidation.createRole), roleController.createRole)
  .get(auth('admin'), roleController.getAllRoles);

router
  .route('/:roleName')
  .get(auth('admin'), validate(roleValidation.getRole), roleController.getRole)
  .patch(auth('admin'), validate(roleValidation.updateRole), roleController.updateRole)
  .delete(auth('admin'), validate(roleValidation.deleteRole), roleController.deleteRole);

module.exports = router;

