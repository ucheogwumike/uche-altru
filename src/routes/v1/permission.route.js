const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const permissionValidation = require('../../validations/permission.validation');
const permissionController = require('../../controllers/permission.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('admin'), validate(permissionValidation.createPermission), permissionController.createPermission)
  .get(auth('admin'), permissionController.getAllPermissions);

router
  .route('/:permissionId')
  .get(auth('admin'), validate(permissionValidation.getPermission), permissionController.getPermission)
  .patch(auth('admin'), validate(permissionValidation.updatePermission), permissionController.updatePermission)
  .delete(auth('admin'), validate(permissionValidation.deletePermission), permissionController.deletePermission);

module.exports = router;

